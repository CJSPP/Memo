public class Memo {
    private int number;
    private String name;
    private String password;
    private String memo;
    private String date;

    public int getNumber() {
        return number;
    }

    public String getName() {
        return name;
    }

    public String getPassword() {
        return password;
    }

    public String getMemo() {
        return memo;
    }

    public String getDate() {
        return date;
    }
}
